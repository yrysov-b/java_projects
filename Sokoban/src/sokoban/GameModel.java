    Maze maze;
    private int curLevel;


    public GameModel() {

        this.curLevel = 0;
        maze = new Maze(levels[curLevel]);

    }


    public void nextLevel() {
        curLevel++;

        if (curLevel == levels.length) {
            curLevel = 0;
        }
        maze = new Maze(levels[curLevel]);
    }

    public int getCurLevel() {
        return curLevel + 1;
    }

    public char[][] getLevels (){
        return levels[curLevel];

    }
